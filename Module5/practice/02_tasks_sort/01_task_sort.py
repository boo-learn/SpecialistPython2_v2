# Сумма с условием
# Дан список чисел.
# Найти: сумму элементов списка, больших данного числа a

numbers = [2.5, 13.6, 13, -22.4, -12.8, 6.7, 12.8, 21, 4, -22.1]
a = 5  # Задайте самостоятельно, выбрав произвольное число
i = 0
summ = 0
while i < len(numbers):
    if numbers[i] > a:
        summ += numbers[i]
    i += 1
print(summ)
